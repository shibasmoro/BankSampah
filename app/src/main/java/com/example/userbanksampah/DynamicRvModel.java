package com.example.userbanksampah;

public class DynamicRvModel {
    String name;
    String harga;


    public DynamicRvModel(String name, String harga) {
        this.name = name;
        this.harga = harga;
    }

    public String getName() {
        return name;
    }

    public String getHarga() {
        return harga;
    }
}
